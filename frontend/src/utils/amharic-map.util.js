export const regexBuilder = (s) => {
    let str = "";
    for (let i = 0; i < s.length; i++) {
        const mappedChar = similarRegexMap[s.charAt(i)];
        if (mappedChar) str += mappedChar;
        else str += s.charAt(i);
    }

    return str;
};
